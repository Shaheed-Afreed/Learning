package com.kodnest.operator;

public class Operator {

	public static void main(String[] args) {
	int a=10;
	int b=5;
	System.out.println("addition result is "+(a+b));
	System.out.println("Subtraction result is "+(a-b));
	System.out.println("Multiplication result is "+(a*b));
	System.out.println("Division quotient  result is "+(a/b));
	System.out.println("Division remainder result is "+(a%b));

	}

}
