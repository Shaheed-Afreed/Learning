package com.kodnest.ComparisionOperator;

public class Comparision {

	public static void main(String[] args) {
		int a=20;
		int b=15;
		int c=20;
		System.out.println("Is a is equal to c "+(a==c));
		System.out.println("Is a is greater than or equal to c "+(a>=c));
		System.out.println("Is b is lesser than or equal to c "+(b<=c));
		System.out.println("Is a is not equal to b "+(a!=b));
	}

}
